for(var x = 0; x < 400; x++){

  var hex = '0123456789ABCDEF';
  var color = '#';

  for (var z = 0; z < 6; z++) {
    color += hex[Math.floor(Math.random() * 16)];
  }

  var node = document.createElement("P");
  var textnode = document.createTextNode("I will change color!");
  node.style.color = color;
  node.appendChild(textnode)
  document.getElementById("re1").appendChild(node);
}

